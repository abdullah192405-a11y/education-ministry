import { readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const fontsDir = path.join(rootDir, "public/fonts/cairo");
const outFile = path.join(rootDir, "src/lib/challengeReportPdfFonts.ts");

const weights = [
  { key: "cairo400", file: "cairo-400.ttf", weight: 400 },
  { key: "cairo600", file: "cairo-600.ttf", weight: 600 },
  { key: "cairo700", file: "cairo-700.ttf", weight: 700 },
];

const entries = weights.map(({ key, file, weight }) => {
  const bytes = readFileSync(path.join(fontsDir, file));
  const base64 = bytes.toString("base64");
  return `  ${key}: { weight: ${weight}, dataUri: "data:font/ttf;base64,${base64}" },`;
});

const output = `/* eslint-disable */
// Generated by scripts/generate-report-fonts.mjs — do not edit manually.

export const CHALLENGE_REPORT_EMBEDDED_FONTS = {
${entries.join("\n")}
} as const;

export function buildEmbeddedReportFontFaces(): string {
  return Object.values(CHALLENGE_REPORT_EMBEDDED_FONTS)
    .map(
      (font) => \`
        @font-face {
            font-family: "Cairo";
            font-style: normal;
            font-weight: \${font.weight};
            font-display: block;
            src: url("\${font.dataUri}") format("truetype");
        }\`
    )
    .join("");
}
`;

writeFileSync(outFile, output, "utf8");
console.log(`Wrote embedded report fonts -> ${outFile}`);
